/**
 * Sync the pure engine + verify helpers into the Supabase Edge Function as
 * Deno-native modules.
 *
 * The Edge Function (Deno) verifies answers, so it needs the SAME engine the app
 * uses (DESIGN.md: one engine, never two). Rather than hand-duplicate it (drift
 * risk), we COPY the pure source into supabase/functions/_shared/engine/ and
 * rewrite the Node-style `.js` import specifiers to `.ts` so Deno resolves them.
 *
 * Run: `npm run sync:edge`. The generated folder is committed so deploys are
 * self-contained, but it is ALWAYS regenerated from src/ — never edit it by hand.
 *
 * Only pure modules are copied (no node:crypto, no React). The Edge verifier
 * uses the browser-safe SHA-256 (verifyhash.ts), same as the app.
 */

import { readFileSync, writeFileSync, mkdirSync, rmSync, readdirSync } from "node:fs";
import { dirname, join } from "node:path";

const ROOT = process.cwd();
const OUT = join(ROOT, "supabase/functions/_shared/engine");

// Pure files the verify path needs. (No node:crypto: we use verifyhash.ts, the
// pure-JS SHA-256 — NOT slowhash.ts which imports node:crypto.)
const FILES = [
  "src/engine/types.ts",
  "src/engine/grid.ts",
  "src/engine/prng.ts",
  "src/engine/clock.ts",
  "src/engine/rule.ts",
  "src/engine/enumerate.ts",
  "src/engine/readout-shape.ts",
  "src/auth/verifier.ts", // Verifier/Credential interfaces (pure)
  "src/auth/canonical.ts",
  "src/auth/verifyhash.ts",
  "src/auth/option-b-verifier.ts",
];

const BANNER = "// AUTO-GENERATED by scripts/sync-engine-to-edge.mjs — do not edit. Run `npm run sync:edge`.\n";

rmSync(OUT, { recursive: true, force: true });

for (const rel of FILES) {
  const src = readFileSync(join(ROOT, rel), "utf8");
  // Flatten src/engine/* and src/auth/* into one folder, so cross-imports like
  // "../engine/types.js" and "./canonical.js" all resolve to siblings here.
  const rewritten = src
    // ../engine/x.js  ->  ./x.ts   ;  ../auth/x.js -> ./x.ts
    .replace(/(['"])\.\.\/(engine|auth)\/([\w-]+)\.js\1/g, "$1./$3.ts$1")
    // ./x.js -> ./x.ts (same-folder)
    .replace(/(['"])\.\/([\w-]+)\.js\1/g, "$1./$2.ts$1");

  const base = rel.split("/").pop();
  const dest = join(OUT, base);
  mkdirSync(dirname(dest), { recursive: true });
  writeFileSync(dest, BANNER + rewritten);
}

// Guard: fail loudly if any copied file IMPORTS node:crypto (not Deno-safe).
// Match import/from statements only, not comments mentioning it.
const NODE_IMPORT = /\b(?:import|from|require)\b[^\n;]*['"]node:/;
for (const f of readdirSync(OUT)) {
  const content = readFileSync(join(OUT, f), "utf8");
  if (NODE_IMPORT.test(content)) {
    throw new Error(`${f} imports a node: builtin — not Deno-safe. Fix the source's imports.`);
  }
}

console.log(`Synced ${FILES.length} engine files → supabase/functions/_shared/engine/`);
